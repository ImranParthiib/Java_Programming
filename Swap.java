public class Swap {
    public static void main(String[] args) {
        System.out.println("Hey bro");

        String x = "water";
        String y = "Kool-Aid";
        String temp;
        temp = x;
        x = y;
        y = temp;
        System.out.println("x:" + x);
        System.out.println("y:" + y);
    }
}
